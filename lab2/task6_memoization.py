def memoize(func):
    """Декоратор-кэш: при повторном вызове с теми же аргументами
    возвращает результат из словаря, не вычисляя заново."""
    cache = {}

    def wrapper(*args):
        if args not in cache:           # если результата нет в кэше
            cache[args] = func(*args)   # вычисляем и сохраняем
        return cache[args]              # возвращаем из кэша

    wrapper.cache = cache               # открываем кэш для инспекции
    wrapper.__name__ = func.__name__
    return wrapper


# ─── Демонстрация декоратора на числах Фибоначчи ─────────────────────────────

@memoize
def fib_memo(n: int) -> int:
    """Números de Fibonacci с мемоизацией."""
    if n <= 1:
        return n
    return fib_memo(n - 1) + fib_memo(n - 2)


def fib_naive(n: int) -> int:
    """Числа Фибоначчи без мемоизации (экспоненциальная сложность)."""
    if n <= 1:
        return n
    return fib_naive(n - 1) + fib_naive(n - 2)


