def is_prime(n: int, divisor: int = 2) -> bool:
    """Рекурсивная проверка числа на простоту.
    Делители проверяются до ⌊√n⌋.

    Терминальные случаи:
        n < 2              → False (не простое)
        divisor² > n       → True  (делителей нет — простое)
        n % divisor == 0   → False (нашли делитель — составное)

    Рекурсивный шаг: проверяем следующий делитель (divisor + 1).
    """
    if n < 2:                          # ← терминальная часть
        return False
    if divisor * divisor > n:          # ← терминальная часть
        return True
    if n % divisor == 0:               # ← терминальная часть
        return False
    return is_prime(n, divisor + 1)   # ← рекурсивный шаг


