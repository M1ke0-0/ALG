def random_multiply(n: float, depth: int = 3) -> float:
    """Рекурсивно умножает n на случайный коэффициент из [0.5, 2.0].

    Терминальный случай: depth == 0 → возвращаем n.
    Рекурсивный шаг: умножаем n на случайное число и уходим глубже.
    """
    if depth == 0:                                       # ← терминальная часть
        return n
    coef = random.uniform(0.5, 2.0)
    print(f"  глубина {depth}: {n:.4f} × {coef:.4f} = {n * coef:.4f}")
    return random_multiply(n * coef, depth - 1)          # ← рекурсивный шаг


