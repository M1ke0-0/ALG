@memoize
def grid_paths(m: int, n: int) -> int:
    """Количество уникальных путей из (0,0) в (m-1,n-1)
    при движении только вправо или вниз.

    Терминальный случай: одна строка или один столбец → ровно 1 путь.
    Рекурсивный шаг: paths(m,n) = paths(m-1,n) + paths(m,n-1).
    """
    if m == 1 or n == 1:               # ← терминальная часть
        return 1
    return grid_paths(m - 1, n) + grid_paths(m, n - 1)   # ← рекурсивный шаг


