# Способ 1: Цикл с использованием стека
def flatten_list_loop(nested_list):
    flat_list = []
    # Копируем список, чтобы не изменять оригинал, стек будет работать как очередь ожидающих обработки элементов
    # Но чтобы сохранить порядок, мы будем использовать стек и добавлять элементы в обратном порядке при распаковке
    stack = [nested_list]
    
    # Мы используем обычный стек, достаем с конца
    # Поэтому чтобы порядок сохранился правильным, кладем элементы задом наперед, 
    # либо используем очередь (deque). Остановимся на стеке для простоты.
    while stack:
        current = stack.pop()
        if isinstance(current, list):
            # Если это список, расширяем стек задом наперед, чтобы сохранить оригинальный порядок после pop()
            stack.extend(current[::-1])
        else:
            # Иначе это атомарный элемент
            flat_list.append(current)
            
    return flat_list

# Способ 2: Рекурсия
def flatten_list_recursive(nested_list):
    flat_list = []
    for item in nested_list:
        if isinstance(item, list):
            # Рекурсивный вызов для вложенного списка
            flat_list.extend(flatten_list_recursive(item))
        else:
            # Терминальная ветка: добавление элемента к уровню
            flat_list.append(item)
    return flat_list

