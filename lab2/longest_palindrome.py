class Solution:
    def longestPalindrome(self, s: str) -> str:
        # Базовый случай: если длина строки 0 или 1, то она уже является палиндромом
        if len(s) <= 1:
            return s
        
        # Переменные для отслеживания наибольшей длины палиндрома и самого палиндрома
        Max_Len = 1
        Max_Str = s[0]
        
        # Внешний цикл проходит по каждому символу в строке (от 0 до предпоследнего)
        # Это начало потенциального палиндрома
        for i in range(len(s) - 1):
            # Внутренний цикл проходит по оставшимся символам, начиная со следующего
            # Это конец потенциального палиндрома
            for j in range(i + 1, len(s)):
                # Если длина текущей подстроки (от i до j) больше максимальной найденной ранеe (Max_Len)
                # И строка s[i:j+1] равна самой себе задом наперед, то есть является палиндромом
                if j - i + 1 > Max_Len and s[i:j+1] == s[i:j+1][::-1]:
                    # Обновляем максимальную длину и самую длинную строку-палиндром
                    Max_Len = j - i + 1
                    Max_Str = s[i:j+1]

        return Max_Str

# --- Подробное объяснение решения (complexity) ---
# Данный код реализует поиск "Самой длинной палиндромной подстроки" переборным решением (Brute Force).
# 1. Циклы i и j перебирают все возможные подстроки длины от 2 до N.
# 2. Выражение s[i:j+1] == s[i:j+1][::-1] проверяет, является ли подстрока палиндромом (читается ли она 
# одинаково слева-направо и справа-налево), причем это занимает O(K), где K - длина подстроки.
# 
# Временная сложность (Time Complexity): O(N^3), так как у нас есть вложенные циклы (O(N^2) итераций) 
# и на каждой итерации делается срез и реверс строки, что в худшем случае занимает O(N) времени.
# Пространственная сложность (Space/Memory Complexity): O(N), так как каждый раз при проверке палиндрома
# мы создаем новую строку (часть оригинальной) с помощью среза s[i:j+1][::-1].
