class DynamicArray:
    """
    Класс, реализующий динамический список на основе массива переменной длины.
    Использует низкоуровневые массивы ctypes для демонстрации реальной работы с памятью.
    """
    def __init__(self):
        self._size = 0           # Текущее число элементов в списке
        self._capacity = 1       # Емкость зарезервированной памяти
        self._A = self._make_array(self._capacity)  # Выделение памяти под массив

    def _make_array(self, c):
        """Возвращает новый массив емкостью c."""
        return (c * ctypes.py_object)()

    def get_size(self):
        """Возвращает текущее количество элементов в списке."""
        return self._size

    def get_capacity(self):
        """Возвращает текущую емкость списка (capacity)."""
        return self._capacity

    def get(self, index):
        """Получение элемента по индексу (с проверкой границ)."""
        if not 0 <= index < self._size:
            raise IndexError("Индекс вышел за пределы допустимого диапазона")
        return self._A[index]

    def append(self, value):
        """Добавление элемента в конец списка."""
        if self._size == self._capacity:
            self._resize(2 * self._capacity)  # При нехватке места удваиваем емкость
        self._A[self._size] = value
        self._size += 1

    def insert(self, index, value):
        """Вставка элемента по индексу."""
        # Проверка корректности индекса для вставки
        if not 0 <= index <= self._size:
            raise IndexError("Индекс для вставки вышел за пределы")
        
        # Если массив заполнен, увеличиваем его емкость в 2 раза
        if self._size == self._capacity:
            self._resize(2 * self._capacity)

        # Сдвигаем элементы вправо, начиная с конца списка до нужного индекса
        for j in range(self._size, index, -1):
            self._A[j] = self._A[j - 1]
        
        # Вставляем элемент
        self._A[index] = value
        self._size += 1

    def remove_at(self, index):
        """Удаление элемента по индексу."""
        if not 0 <= index < self._size:
            raise IndexError("Индекс для удаления вышел за пределы")
        
        removed_value = self._A[index]
        
        # Сдвигаем элементы влево, перекрывая удаленный элемент
        for j in range(index, self._size - 1):
            self._A[j] = self._A[j + 1]
            
        self._A[self._size - 1] = None  # Очищаем ссылку для сборщика мусора
        self._size -= 1
        return removed_value

    def _resize(self, c):
        """Внутренний метод для изменения размера массива до емкости c."""
        print(f"-> Изменение емкости: {self._capacity} -> {c}")
        B = self._make_array(c)  # Создаем новый массив большего размера
        for k in range(self._size):  # Копируем старые элементы в новый массив
            B[k] = self._A[k]
        self._A = B  # Перенаправляем ссылку на новый массив
        self._capacity = c

    def to_list(self):
        """Вспомогательный метод для удобного вывода содержимого списка."""
        return [self._A[i] for i in range(self._size)]

    def __str__(self):
        return str(self.to_list())

    def __getitem__(self, index):
        """Поддержка стандартного синтаксиса получения элемента по индексу [index]."""
        return self.get(index)


