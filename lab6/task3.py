from task2 import SinglyLinkedList

class LinkedStack:
    """Стек на основе односвязного списка (LIFO)."""
    def __init__(self):
        self._list = SinglyLinkedList()  # Используем список из задания 2

    def push(self, value):
        """Добавление элемента в вершину стека (O(1))."""
        self._list.add_to_front(value)

    def pop(self):
        """Удаление и возвращение элемента из вершины стека (O(1))."""
        if self.is_empty():
            raise IndexError("Попытка извлечь элемент из пустого стека")
        return self._list.remove_first()

    def peek(self):
        """Получение элемента из вершины стека без его удаления (O(1))."""
        if self.is_empty():
            raise IndexError("Попытка посмотреть элемент в пустом стеке")
        return self._list.get(0)

    def size(self):
        """Возвращает текущее количество элементов в стеке."""
        return self._list.get_size()

    def is_empty(self):
        """Проверка стека на пустоту."""
        return self._list.get_size() == 0

    def __str__(self):
        # Показываем стек сверху вниз (от головы списка к концу)
        if self.is_empty():
            return "Пустой стек"
        return " -> ".join(str(self._list.get(i)) for i in range(self._list.get_size())) + " (Вершина слева)"


