"""
Задание 1. Реализация алгоритмов поиска
- Модифицированный линейный поиск (с барьерным элементом)
- Бинарный поиск
- Интерполяционный поиск
- Экспоненциальный поиск
"""


# ─────────────────────────────────────────────
# 1. Модифицированный линейный поиск (с барьером)
# ─────────────────────────────────────────────
def linear_search_barrier(arr: list, target) -> int:
    """
    Линейный поиск с барьерным элементом.
    Добавляем target в конец массива → гарантированно найдём,
    поэтому не нужна проверка границы на каждом шаге.
    Возвращает индекс или -1, если элемент отсутствует.
    """
    n = len(arr)
    arr_copy = arr + [target]   # барьерный элемент в конце
    i = 0
    while arr_copy[i] != target:
        i += 1
    if i < n:
        return i
    return -1  # нашли только барьер → элемент отсутствует


# ─────────────────────────────────────────────
# 2. Бинарный поиск (только для отсортированных)
# ─────────────────────────────────────────────
def binary_search(arr: list, target) -> int:
    """
    Классический бинарный поиск.
    Требует отсортированного массива.
    Возвращает индекс или -1.
    """
    lo, hi = 0, len(arr) - 1
    while lo <= hi:
        mid = (lo + hi) // 2
        if arr[mid] == target:
            return mid
        elif arr[mid] < target:
            lo = mid + 1
        else:
            hi = mid - 1
    return -1


# ─────────────────────────────────────────────
# 3. Интерполяционный поиск (только для отсортированных)
# ─────────────────────────────────────────────
def interpolation_search(arr: list, target) -> int:
    """
    Интерполяционный поиск.
    Вычисляет предполагаемую позицию по формуле линейной интерполяции.
    Эффективен при равномерном распределении значений.
    Возвращает индекс или -1.
    """
    lo, hi = 0, len(arr) - 1
    while lo <= hi and arr[lo] <= target <= arr[hi]:
        if arr[lo] == arr[hi]:      # все элементы одинаковы
            return lo if arr[lo] == target else -1
        # формула интерполяции
        pos = lo + (target - arr[lo]) * (hi - lo) // (arr[hi] - arr[lo])
        pos = max(lo, min(hi, pos))  # гарантируем попадание в [lo, hi]
        if arr[pos] == target:
            return pos
        elif arr[pos] < target:
            lo = pos + 1
        else:
            hi = pos - 1
    return -1


# ─────────────────────────────────────────────
# 4. Экспоненциальный поиск (только для отсортированных)
# ─────────────────────────────────────────────
def exponential_search(arr: list, target) -> int:
    """
    Экспоненциальный поиск.
    Сначала удваивает диапазон, пока arr[i] < target,
    затем выполняет бинарный поиск в найденном диапазоне.
    Хорош для неограниченных/ очень больших массивов.
    Возвращает индекс или -1.
    """
    n = len(arr)
    if n == 0:
        return -1
    if arr[0] == target:
        return 0
    # экспоненциальное расширение диапазона
    i = 1
    while i < n and arr[i] <= target:
        i *= 2
    # бинарный поиск в диапазоне [i//2, min(i, n-1)]
    lo = i // 2
    hi = min(i, n - 1)
    while lo <= hi:
        mid = (lo + hi) // 2
        if arr[mid] == target:
            return mid
        elif arr[mid] < target:
            lo = mid + 1
        else:
            hi = mid - 1
    return -1


