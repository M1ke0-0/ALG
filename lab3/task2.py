def find_max_subarray(profits):
    max_so_far = float('-inf')
    current_max = 0
    start_idx = 0
    end_idx = 0
    temp_start = 0

    for i, profit in enumerate(profits):
        current_max += profit
        
        if current_max > max_so_far:
            max_so_far = current_max
            start_idx = temp_start
            end_idx = i
            
        if current_max < 0:
            current_max = 0
            temp_start = i + 1

    return max_so_far, profits[start_idx:end_idx+1], start_idx, end_idx

